defmodule TestLens.Dashboard do
  @moduledoc """
  Generates the TestLens dashboard — a single-file, zero-dependency
  static SPA that renders TestLens artifacts in the browser.

  The dashboard reads `_build/test_lens/agent.json` and
  `_build/test_lens/advice.json` (when present) and renders them as a
  tabbed view: Summary / Failures / Repair Queue / OTP Snapshots /
  Architecture Findings.

  No build step. No JS framework. No external assets. The output is a
  single `index.html` file plus an `app.js` and `app.css`, all
  self-contained.
  """

  @default_dir "_build/test_lens/dashboard"
  @index_filename "index.html"
  @app_js_filename "app.js"
  @app_css_filename "app.css"

  @doc "Returns the default dashboard output directory."
  @spec default_dir() :: String.t()
  def default_dir, do: @default_dir

  @doc """
  Generate the dashboard artifacts into `dir` (defaults to
  `_build/test_lens/dashboard`). Creates `dir` if it does not exist.
  """
  @spec generate(Path.t()) :: :ok | {:error, term()}
  def generate(dir \\ @default_dir) do
    File.mkdir_p!(dir)

    results = [
      write_file(Path.join(dir, @index_filename), index_html()),
      write_file(Path.join(dir, @app_js_filename), app_js()),
      write_file(Path.join(dir, @app_css_filename), app_css())
    ]

    if Enum.all?(results, &(&1 == :ok)),
      do: :ok,
      else: Enum.find(results, &match?({:error, _}, &1))
  end

  defp write_file(path, content) do
    File.write(path, content)
  rescue
    e -> {:error, Exception.message(e)}
  end

  # ---------------------------------------------------------------------------
  # HTML
  # ---------------------------------------------------------------------------

  defp index_html do
    """
    <!doctype html>
    <html lang="en">
      <head>
        <meta charset="utf-8">
        <meta name="viewport" content="width=device-width, initial-scale=1">
        <title>TestLens Dashboard</title>
        <link rel="stylesheet" href="app.css">
      </head>
      <body>
        <header>
          <h1>TestLens Dashboard</h1>
          <p id="meta">Loading artifacts from <code id="base-path"></code>…</p>
        </header>

        <nav>
          <button data-tab="summary" class="tab active">Summary</button>
          <button data-tab="failures" class="tab">Failures</button>
          <button data-tab="repair" class="tab">Repair Queue</button>
          <button data-tab="otp" class="tab">OTP Snapshots</button>
          <button data-tab="arch" class="tab">Architecture Findings</button>
        </nav>

        <main id="content">Loading…</main>

        <footer>
          <p>Generated by <code>TestLens.Dashboard</code>. No external assets. No tracking.</p>
        </footer>

        <script src="app.js"></script>
      </body>
    </html>
    """
  end

