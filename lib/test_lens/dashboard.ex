  # ---------------------------------------------------------------------------
  # JS
  # ---------------------------------------------------------------------------

  defp app_js do
    """
    (function () {
      "use strict";

      const $ = (sel) => document.querySelector(sel);
      const $$ = (sel) => Array.from(document.querySelectorAll(sel));

      const state = {
        activeTab: "summary",
        agent: null,
        advice: null,
        loadErrors: []
      };

      async function fetchJSON(path) {
        try {
          const res = await fetch(path);
          if (!res.ok) throw new Error("HTTP " + res.status);
          return await res.json();
        } catch (e) {
          state.loadErrors.push(path + ": " + e.message);
          return null;
        }
      }

      function el(tag, attrs, children) {
        const node = document.createElement(tag);
        if (attrs) {
          for (const [k, v] of Object.entries(attrs)) {
            if (k === "class") node.className = v;
            else if (k === "text") node.textContent = v;
            else if (k.startsWith("data-")) node.setAttribute(k, v);
            else if (k === "html") node.innerHTML = v;
            else node.setAttribute(k, v);
          }
        }
        if (children) {
          for (const c of children) {
            if (typeof c === "string") node.appendChild(document.createTextNode(c));
            else if (c) node.appendChild(c);
          }
        }
        return node;
      }

      function severityClass(s) {
        return "severity-" + s;
      }

      function renderSummary() {
        const a = state.agent;
        if (!a) return el("p", { class: "empty" }, ["No agent artifact loaded."]);
        const t = a.totals || {};
        const out = el("div", null, [
          el("div", { class: "card" }, [
            el("h2", null, ["Run"]),
            el("p", null, [
              el("span", { class: "muted" }, ["Schema "]),
              el("code", null, [a.schema_version || "?"]),
              el("span", { class: "muted" }, [" · TestLens "]),
              el("code", null, [a.test_lens_version || "?"])
            ]),
            el("p", null, [el("code", null, [JSON.stringify(a.run || {}, null, 2)])])
          ]),
          el("div", { class: "card" }, [
            el("h2", null, ["Totals"]),
            el("div", { class: "grid" }, [
              stat("tests", t.tests),
              stat("passed", t.passed),
              stat("failed", t.failed),
              stat("skipped", t.skipped),
              stat("invalid", t.invalid),
              stat("excluded", t.excluded)
            ])
          ])
        ]);
        if (a.repair_queue) {
          out.appendChild(el("div", { class: "card" }, [
            el("h2", null, ["Repair Queue (" + a.repair_queue.length + ")"]),
            el("p", { class: "muted" }, ["Ranked by priority + confidence. See Repair Queue tab for details."])
          ]));
        }
        if (a.otp_snapshots && a.otp_snapshots.length > 0) {
          out.appendChild(el("div", { class: "card" }, [
            el("h2", null, ["OTP Snapshots (" + a.otp_snapshots.length + ")"]),
            el("p", { class: "muted" }, ["Captured at the moment each test failed. See OTP Snapshots tab."])
          ]));
        }
        if (a.architecture_findings && a.architecture_findings.length > 0) {
          out.appendChild(el("div", { class: "card" }, [
            el("h2", null, ["Architecture Findings (" + a.architecture_findings.length + ")"]),
            el("p", { class: "muted" }, ["Static AST + supervisor topology findings. See Architecture tab."])
          ]));
        }
        return out;
      }

      function stat(label, n) {
        return el("div", { class: "stat" }, [
          el("div", { class: "num" }, [String(n == null ? "?" : n)]),
          el("div", { class: "label" }, [label])
        ]);
      }

      function renderFailures() {
        const a = state.agent;
        if (!a || !a.failures) return el("p", { class: "empty" }, ["No failures."]);
        if (a.failures.length === 0) return el("p", { class: "empty" }, ["No failures in this run."]);
        return el("div", null, a.failures.map(f => {
          const det = el("details", null, [
            el("summary", null, [
              el("span", { class: severityClass(f.severity) }, [(f.severity || "?").toUpperCase()]),
              " ",
              el("strong", null, [f.module || "?"]),
              " > ",
              el("span", null, [f.name || "?"])
            ]),
            el("div", null, [
              el("p", null, [el("span", { class: "muted" }, ["id: "]), el("code", null, [f.id || "?"])]),
              el("p", null, [el("span", { class: "muted" }, ["file: "]), el("code", null, [f.file || "?"])]),
              f.classification ? el("pre", null, [JSON.stringify(f.classification, null, 2)]) : null,
              f.impact ? el("pre", null, [JSON.stringify(f.impact, null, 2)]) : null,
              f.app_stacktrace ? el("pre", null, [JSON.stringify(f.app_stacktrace, null, 2)]) : null,
              f.hypotheses ? el("div", null, f.hypotheses.map(h => el("p", null, [h.summary || JSON.stringify(h)]))) : null,
              f.rerun_command ? el("p", null, [el("code", null, [f.rerun_command])]) : null
            ])
          ]);
          return det;
        }));
      }

      function renderRepair() {
        const a = state.agent;
        if (!a || !a.repair_queue) return el("p", { class: "empty" }, ["No repair queue."]);
        if (a.repair_queue.length === 0) return el("p", { class: "empty" }, ["Repair queue is empty."]);
        return el("div", null, a.repair_queue.map(r => el("div", { class: "card" }, [
          el("h2", null, [
            el("span", { class: severityClass(r.priority) }, [(r.priority || "?").toUpperCase()]),
            " ",
            r.summary || r.id
          ]),
          el("p", { class: "muted" }, ["confidence: " + (r.confidence || "?")]),
          r.evidence && r.evidence.length ? el("div", null, [el("strong", null, ["Evidence"]), el("ul", null, r.evidence.map(e => el("li", null, [e])))]) : null,
          r.first_checks && r.first_checks.length ? el("div", null, [el("strong", null, ["First checks"]), el("ul", null, r.first_checks.map(c => el("li", null, [c])))]) : null,
          r.likely_files && r.likely_files.length ? el("div", null, [el("strong", null, ["Likely files"]), el("ul", null, r.likely_files.map(f => el("li", null, [el("code", null, [f])])))]) : null,
          r.verification_commands && r.verification_commands.length ? el("div", null, [el("strong", null, ["Verify"]), el("ul", null, r.verification_commands.map(c => el("li", null, [el("code", null, [c])])))]) : null
        ])));
      }

      function renderOTP() {
        const a = state.agent;
        if (!a || !a.otp_snapshots) return el("p", { class: "empty" }, ["No OTP snapshots."]);
        if (a.otp_snapshots.length === 0) return el("p", { class: "empty" }, ["OTP snapshots were not captured for this run. Re-run with --snapshot."]);
        return el("div", null, a.otp_snapshots.map(s => el("details", { class: "card" }, [
          el("summary", null, [s.snapshot_id + " — " + (s.test_module || "?") + " > " + (s.test_name || "?")]),
          el("div", null, [
            el("p", null, [el("span", { class: "muted" }, ["captured_at: "]), el("code", null, [s.captured_at || "?"])]),
            s.process_info ? el("pre", null, [JSON.stringify(s.process_info, null, 2)]) : null,
            s.supervision_subtree && s.supervision_subtree.length ? el("div", null, [el("strong", null, ["Supervision subtree"]), el("pre", null, [JSON.stringify(s.supervision_subtree, null, 2)])]) : null,
            s.telemetry_events && s.telemetry_events.length ? el("div", null, [el("strong", null, ["Telemetry events (" + s.telemetry_events.length + ")"]), el("pre", null, [JSON.stringify(s.telemetry_events.slice(0, 10), null, 2)])]) : null
          ])
        ])));
      }

      function renderArch() {
        const findings = (state.agent && state.agent.architecture_findings) || (state.advice && state.advice.findings) || [];
        if (findings.length === 0) return el("p", { class: "empty" }, ["No architecture findings. Re-run with --advise."]);
        return el("div", null, findings.map(f => el("div", { class: "card" }, [
          el("h2", null, [
            el("span", { class: severityClass(f.severity) }, [(f.severity || "?").toUpperCase()]),
            " ",
            f.rule_id || "?",
            el("span", { class: "muted" }, [" — confidence " + (f.confidence || "?")])
          ]),
          el("p", null, [el("code", null, [f.id || "?"])]),
          f.evidence ? el("p", null, [f.evidence]) : null,
          f.explanation ? el("p", { class: "muted" }, [f.explanation]) : null,
          f.remediation ? el("p", null, [el("strong", null, ["Remediation: "]), f.remediation]) : null,
          f.related_modules && f.related_modules.length ? el("p", null, [el("strong", null, ["Related: "]), f.related_modules.map(m => el("span", { class: "tag" }, [m]))]) : null,
          f.location && (f.location.file || f.location.line) ? el("p", null, [el("span", { class: "muted" }, ["at "]), el("code", null, [(f.location.file || "?") + ":" + (f.location.line || "?")])]) : null
        ])));
      }

      const renderers = {
        summary: renderSummary,
        failures: renderFailures,
        repair: renderRepair,
        otp: renderOTP,
        arch: renderArch
      };

      function render() {
        const main = $("#content");
        main.innerHTML = "";
        const fn = renderers[state.activeTab] || renderSummary;
        main.appendChild(fn());
      }

      async function load() {
        const base = location.pathname.endsWith("/") ? location.pathname : location.pathname.replace(/[^/]*$/, "");
        $("#base-path").textContent = base;
        state.agent = await fetchJSON(base + "../agent.json");
        state.advice = await fetchJSON(base + "../advice.json");

        if (state.loadErrors.length > 0) {
          const meta = $("#meta");
          const warn = el("p", { class: "muted" }, ["⚠ " + state.loadErrors.join("; ")]);
          meta.appendChild(warn);
        }

        render();
      }

      function switchTab(name) {
        state.activeTab = name;
        $$(".tab").forEach(b => b.classList.toggle("active", b.dataset.tab === name));
        render();
      }

      $$(".tab").forEach(b => b.addEventListener("click", () => switchTab(b.dataset.tab)));
      window.addEventListener("DOMContentLoaded", load);
      if (document.readyState !== "loading") load();
    })();
    """
  end
end
